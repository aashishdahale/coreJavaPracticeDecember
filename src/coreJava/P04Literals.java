package coreJava;

public class P04Literals {

	public static void main(String[] args) {
		
		int num1 = 0b101;
		int num2 = 0x7E;
		int num3 = 100_00_00_000;
		double num4 = 12e10;
		
		char c = 'a';

		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		
		c++;
		System.out.println(c);
		
	}

}
